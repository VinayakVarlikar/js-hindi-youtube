const month = "Mar";
switch (month) {
  case "jan":
    console.log("January");
    break;
  case "feb":
    console.log("feb");
    break;
  case "Mar":
    console.log("Mar");
    break;
  case 4:
    console.log("April");
    break;

  default:
    console.log("default caase match");
    break;
}
